export interface Instruction {
    opcode: number;
    name: string;
    pop: number;
    push: number;
    popString: number;
    pushString: number;
    popTypes?: string[];
    pushTypes?: string[];
}

export class OpcodeDb {
    private instructions = new Map<number, Instruction>();
    private instructionsByName = new Map<string, Instruction>();

    add(
        opcode: number,
        name: string,
        pop = 0,
        push = 0,
        popString = 0,
        pushString = 0,
        popTypes?: string[],
        pushTypes?: string[],
    ): void {
        const i = { opcode, name, pop, push, popString, pushString, popTypes, pushTypes };
        this.instructions.set(opcode, i);
        if (name) {
            this.instructionsByName.set(name, i);
        }
    }

    find(opcode: number): Instruction | undefined {
        return this.instructions.get(opcode);
    }

    findByName(name: string): Instruction | undefined {
        return this.instructionsByName.get(name);
    }
}

